            set;
        }
        //WPOLRZEDNE LEWY GORNY ROG
        public CialoNiebieskie(float x, float y, float r)
        {
            X = x;
            Y = y;
            R = r;
        }

        //odleglosc pomiedzy sferami
        public double dl(CialoNiebieskie a, CialoNiebieskie b)
        {
            return Math.Sqrt(Math.Pow(a.SX - b.SX, 2) + Math.Pow(a.SY - b.SY, 2));
        }

        //true kolizja itp
        public Boolean kolizja(CialoNiebieskie a, CialoNiebieskie b)
        {
            return dl( a, b ) <= a.R + b.R;
        }
